const grid = document.querySelector("[data-article-grid]");
const searchInput = document.querySelector("[data-search-input]");
const filterButtons = document.querySelectorAll("[data-filter]");
const emptyState = document.querySelector("[data-empty-state]");
const dialog = document.querySelector("[data-article-dialog]");
const navLinks = document.querySelector("[data-nav-links]");
const menuToggle = document.querySelector("[data-menu-toggle]");
const backToTop = document.querySelector("[data-back-to-top]");
const progress = document.querySelector("[data-reading-progress]");
const bookmarkKey = "insightlab:favorites";
let activeFilter = "todos";
let currentArticleId = null;

const getFavorites = () => JSON.parse(localStorage.getItem(bookmarkKey) || "[]");
const setFavorites = (ids) => localStorage.setItem(bookmarkKey, JSON.stringify(ids));

function articleMatches(article, query) {
  const haystack = `${article.title} ${article.category} ${article.summary}`.toLowerCase();
  return haystack.includes(query.trim().toLowerCase());
}

function renderArticles() {
  const query = searchInput.value;
  const favorites = getFavorites();
  const filtered = articles.filter((article) => {
    const categoryMatch = activeFilter === "todos" || article.category === activeFilter;
    return categoryMatch && articleMatches(article, query);
  });

  grid.innerHTML = filtered.map((article) => {
    const isFavorite = favorites.includes(article.id);
    return `
      <article class="article-card">
        <img src="${article.image}" alt="${article.alt}" loading="lazy">
        <div class="card-body">
          <span class="tag">${article.category}</span>
          <h3>${article.title}</h3>
          <p>${article.summary}</p>
          <div class="article-meta">
            <span><i data-lucide="clock-3"></i> ${article.readTime}</span>
            <span><i data-lucide="calendar"></i> ${article.date}</span>
          </div>
          <div class="card-actions">
            <button class="read-link" type="button" data-open-article="${article.id}">Ler artigo</button>
            <button class="icon-button bookmark-button ${isFavorite ? "active" : ""}" type="button" aria-label="Favoritar artigo" data-bookmark="${article.id}">
              <i data-lucide="bookmark"></i>
            </button>
          </div>
        </div>
      </article>
    `;
  }).join("");

  emptyState.hidden = filtered.length > 0;
  if (window.lucide) {
    window.lucide.createIcons();
  }
}

function bodyToHtml(body) {
  return body.map(([type, content]) => {
    if (type === "h3") return `<h3>${content}</h3>`;
    if (type === "ul") return `<ul>${content.map((item) => `<li>${item}</li>`).join("")}</ul>`;
    return `<p>${content}</p>`;
  }).join("");
}

function openArticle(id) {
  const article = articles.find((item) => item.id === id);
  if (!article) return;
  currentArticleId = id;

  dialog.querySelector("[data-dialog-image]").src = article.image;
  dialog.querySelector("[data-dialog-image]").alt = article.alt;
  dialog.querySelector("[data-dialog-category]").textContent = article.category;
  dialog.querySelector("[data-dialog-readtime]").textContent = `${article.readTime} de leitura`;
  dialog.querySelector("[data-dialog-title]").textContent = article.title;
  dialog.querySelector("[data-dialog-summary]").textContent = article.summary;
  dialog.querySelector("[data-dialog-body]").innerHTML = bodyToHtml(article.body);
  updateDialogBookmark();

  dialog.showModal();
  document.body.classList.add("dialog-open");
  if (window.lucide) {
    window.lucide.createIcons();
  }
}

function closeDialog() {
  dialog.close();
  document.body.classList.remove("dialog-open");
}

function toggleFavorite(id) {
  const favorites = getFavorites();
  const next = favorites.includes(id)
    ? favorites.filter((item) => item !== id)
    : [...favorites, id];
  setFavorites(next);
  renderArticles();
  updateDialogBookmark();
}

function updateDialogBookmark() {
  const button = dialog.querySelector("[data-bookmark-current]");
  const isFavorite = getFavorites().includes(currentArticleId);
  button.classList.toggle("active", isFavorite);
  button.innerHTML = `<i data-lucide="bookmark"></i>${isFavorite ? "Favoritado" : "Favoritar"}`;
}

async function shareCurrentArticle() {
  const article = articles.find((item) => item.id === currentArticleId);
  if (!article) return;
  const shareData = {
    title: article.title,
    text: article.summary,
    url: `${location.href.split("#")[0]}#${article.id}`
  };

  if (navigator.share) {
    await navigator.share(shareData);
  } else {
    await navigator.clipboard.writeText(shareData.url);
    const button = dialog.querySelector("[data-share-current]");
    button.innerHTML = `<i data-lucide="check"></i>Link copiado`;
    setTimeout(() => {
      button.innerHTML = `<i data-lucide="share-2"></i>Compartilhar`;
      if (window.lucide) window.lucide.createIcons();
    }, 1800);
  }
}

filterButtons.forEach((button) => {
  button.addEventListener("click", () => {
    activeFilter = button.dataset.filter;
    filterButtons.forEach((item) => item.classList.toggle("active", item === button));
    renderArticles();
  });
});

searchInput.addEventListener("input", renderArticles);

document.addEventListener("click", (event) => {
  const openButton = event.target.closest("[data-open-article]");
  const bookmarkButton = event.target.closest("[data-bookmark]");

  if (openButton) {
    openArticle(openButton.dataset.openArticle);
  }

  if (bookmarkButton) {
    toggleFavorite(bookmarkButton.dataset.bookmark);
  }
});

document.querySelector("[data-close-dialog]").addEventListener("click", closeDialog);
dialog.addEventListener("click", (event) => {
  if (event.target === dialog) closeDialog();
});

document.querySelector("[data-bookmark-current]").addEventListener("click", () => {
  if (currentArticleId) toggleFavorite(currentArticleId);
});

document.querySelector("[data-share-current]").addEventListener("click", shareCurrentArticle);

document.querySelector("[data-theme-toggle]").addEventListener("click", () => {
  const isDark = document.documentElement.classList.toggle("dark");
  localStorage.setItem("insightlab:theme", isDark ? "dark" : "light");
});

menuToggle.addEventListener("click", () => {
  const isOpen = navLinks.classList.toggle("open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
});

document.querySelectorAll(".nav-links a").forEach((link) => {
  link.addEventListener("click", () => {
    navLinks.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
  });
});

document.querySelector("[data-newsletter-form]").addEventListener("submit", (event) => {
  event.preventDefault();
  const email = new FormData(event.currentTarget).get("email");
  localStorage.setItem("insightlab:newsletter", String(email));
  document.querySelector("[data-form-message]").textContent = "Inscricao registrada com sucesso neste navegador.";
  event.currentTarget.reset();
});

